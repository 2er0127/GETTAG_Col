package com.gettag.vo;

public class GameInfoVO {
	private String gTitle;
	private String gRelease;
	private String gImage;
	private String gAppid;
	
	public String getgTitle() {
		return gTitle;
	}
	public void setgTitle(String gTitle) {
		this.gTitle = gTitle;
	}
	public String getgRelease() {
		return gRelease;
	}
	public void setgRelease(String gRelease) {
		this.gRelease = gRelease;
	}
	public String getgImage() {
		return gImage;
	}
	public void setgImage(String gImage) {
		this.gImage = gImage;
	}
	public String getgAppid() {
		return gAppid;
	}
	public void setgAppid(String gAppid) {
		this.gAppid = gAppid;
	}
	
	
}
